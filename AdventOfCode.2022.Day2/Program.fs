// part 1
let calculate (inputs : string list) =
    let rec scoreRow (inputs: string list) (total: int) =
        if inputs = [] then
            total
        else
            let row = inputs.Head
            match row[0] with
            | 'A' ->
                let outcome = if row[2] = 'Y' then 8 else if row[2] = 'Z' then 3 else 4
                scoreRow inputs.Tail (total + outcome)
            | 'B' ->
                let outcome = if row[2] = 'Z' then 9 else if row[2] = 'X' then 1 else 5
                scoreRow inputs.Tail (total + outcome)
            | 'C' ->
                let outcome = if row[2] = 'X' then 7 else if row[2] = 'Y' then 2 else 6
                scoreRow inputs.Tail (total + outcome)
            | _ -> total
    scoreRow inputs 0
    
